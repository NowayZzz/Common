package zp.com.test;

public class Student {
	private int age;
	private String id;
	
	
	public Student() {
		super();
	}
	public String Student() {
		return "";
	}
	public Student(int age, String id) {
		super();
		this.age = age;
		this.id = id;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
}
